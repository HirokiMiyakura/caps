    <h4 class="pt-4 pb-2">家族信託コーディネーター</h4>

    <p>お客様（所有者とそのご家族）との相談窓口としてヒアリングを重ね、どのような信託の仕組みを構築すべきかの企画・設計を行い、必要に応じて専門家をアレンジする役割です。<br><br>
    税理士・司法書士等の専門士業の方々がこの家族信託コーディネートを担われるケースも多々ありますが、一方で、お客様と最も強固な信頼関係を築き、お客様の家族関係やご家族の状況、保有資産内容を把握できている保険業界（保険代理店、生命保険のプランナーなど）、 不動産業界（管理会社、宅建業者、ハウスメーカーなど）、介護業界（ケアマネージャー、有料老人ホームなど）の方々などがその役割を担われることも多くなってきております。<br><br>
    家族信託コーディネーターは、お客様の生い立ちやご家族関係、経済状況、保有資産、将来予測されるご家族の出来事（結婚、出産、養子縁組、離婚等）など様々な情報をしっかりとヒアリングし、ご本人及びご家族の“想い”を整理させて頂く重要な役割となります。</p>

    <h4 class="pt-4 pb-2">家族信託専門士</h4>

    <p>ご家族内で合意された信託内容を、法律面あるいは税務面でのチェックを行い、信託内容を反映した契約書面や公正証書の作成など、専門士業がそれぞれの職責で実務を行います。<br><br>
    どのような信託を組成するかによって、どのような実務者が必要となるかは変わって参ります。上記家族信託コーディネーターが必要となる専門家と連携して信託組成に取り組むことになります。</p>

