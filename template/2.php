    <h2 class="mt-4 py-4 border-bottom">家族信託って何？</h2>

    <h3 class="py-3 mt-5 mb-3">「家族信託」の定義</h3>
    <p>「家族信託」とは、一言でいうと『財産管理の一手法』です。<br><br>
    資産を持つ方が、特定の目的（例えば「自分の老後の生活・介護等に必要な資金の管理及び給付」等）に従って、その保有する不動産・預貯金等の資産を信頼できる家族に託し、その管理・処分を任せる仕組みです。いわば、「家族の家族による家族のための信託（財産管理）」と言えます。<br><br>
    家族・親族に管理を託すので、高額な報酬は発生しません。したがって、資産家のためのものでなく、誰にでも気軽に利用できる仕組みです。</p>

