    <h2 class="mt-4 py-4 border-bottom">遺言とは？遺言の効力や遺言の基礎知識を解説</h2>
    <p>「遺言」とは被相続人の最終的な意思を示すもので、相続や相続税対策を考えるときにあったほうがよいものです。遺言を残そうと思っている人はどうすればよいのか、相続人になり遺言書を見つけたらどうすればよいのか、将来の被相続人と相続人の双方にとって必要な基礎知識を確認しておきましょう。</p>

    <h3 class="py-3 mt-5 mb-3">遺言とは？</h3>
    <p>「遺言」とは、被相続人（遺言者）が自分の財産について誰に何を残したいのか、最終の意思表示をするものです。遺言は日常的には「ゆいごん」と読みますが、法律用語としては「いごん」と読みます。<br><br>
    遺言を書面にしたものが遺言書です。遺言書があれば、原則としてその内容のとおりに遺産を分割するルールになっているので、相続人間の争いが起こりにくくなります。相続財産の換金や売却などもスムーズにできるので、相続人は相続税を支払いやすくなります。<br><br>
    また、遺言書を作れば、遺産を法定相続人だけでなく自分が財産をあげたいと思う人に残すことや寄付をすることもできます。介護をしてくれた長男の嫁に財産を残したい、内縁の妻に財産を残したいというケースなどが該当します。</p>

    <h3 class="py-3 mt-5 mb-3">遺言のすすめ</h3>
    <p>相続をめぐる争いが増えています。<br>
司法統計でも家庭裁判所へ持ち込まれる相続に関連する相談件数や新たに申し立てられる調停の件数が右肩上がりに増加していることが報告されています。<br><br>
いったん相続をめぐる争いが起きると相続人の感情的対立に発展し、親族関係が断絶状態になることも少なくありません。<br>
相続をめぐる争いを避ける最も有効な手段は遺言を残すことです。</p>

    <h3 class="py-3 mt-5 mb-3">法的効力を持つ項目「遺言事項」とは</h3>
    <p>遺言書に法的な効果を確保させるには、意思表示の内容を法律上定められた３つの方法（公正証書遺言、自筆証書遺言、秘密証書遺言。詳細は後述）で作成する必要があります。そのうえで法的な効力を持つ項目は「遺言事項」として民法により定められていますので、まずは遺言事項とその例を確認しましょう。</p>

    <h4 class="pt-4 pb-2">遺言事項とその例</h4>
    <h5 class="mt-2">①相続に関する事項</h5>
    <ol>
      <li>誰がどの財産をどのくらい相続できるか指定できる</li>
      <li>法定相続人以外の人にも財産を残せる（遺贈）</li>
      <li>法定相続分とは異なる相続分割合の指定</li>
      <li>特別受益の持ち戻しの免除</li>
      <li>相続人の廃除、廃除の取り消し</li>
    </ol>

    <h5 class="mt-2">②財産の処分に関する事項</h5>
    <ol>
      <li>財産の寄付の指定</li>
    </ol>

    <h5 class="mt-2">③身分に関する事項</h5>
    <ol>
      <li>婚姻外の子供の認知</li>
      <li>未成年の子供の後見人の指定</li>
    </ol>

    <h5 class="mt-2">④遺言執行に関する事項</h5>
    <ol>
      <li>「遺言執行者」の指定</li>
    </ol>

    <h4 class="pt-4 pb-2">遺言執行者について</h4>
    <p>遺言の内容を実現することを「遺言の執行」といいます。遺言の執行には様々な手続きが伴います。たとえば被相続人の預金口座の解約手続き、不動産の所有権移転登記の手続きとそれに続く相続人への名義変更の手続きなどです。こうした遺言の執行に関わる一切の行為の権利と義務を有するのが「遺言執行者」です。<br><br>
    遺言執行者を立てずに遺言を執行しようとすると、一つひとつの財産の相続について相続人全員分の署名押印、手続きへの立会いなどの手間が発生し、遺言の執行がなかなか進まなくなります。一方、遺言執行者を決めておけば、相続手続きの一切を任せることができるので、遺言の執行がスムーズに進むという効果があります。したがって、遺言書を作成するなら遺言執行者を立てるほうがよいでしょう。<br><br>
    法律上、遺言執行者を立てなければならないケースもあります。それは遺言の内容に「推定相続人の廃除」「子の認知」を記す場合です。推定相続人の廃除とは、相続する権利を有する人の権利を剥奪することです。「自分の面倒をみなかった長男には遺産を相続させない」といったように、特定の相続人に遺産を渡さないことを意味します。子の認知とは、婚姻外の子ども（非嫡出子）を自分の子どもだと認めることです。<br><br>
    遺言執行者を指定する方法には主に次の３つがあります。</p>
    <ol>
      <li>遺言者（被相続人）が遺言書に誰を遺言執行者にするか記載する。</li>
      <li>遺言者が遺言書に「第三者に遺言執行者を指定してもらうこと」を記載する。</li>
      <li>遺言者の指定がない場合、相続人等の申立てにより家庭裁判所が選任する。</li>
    </ol>
    <p>遺言執行者には相続人や親族もなれますが、弁護士や税理士など利害関係のない第三者に依頼したほうが無難でしょう。</p>

    <h3 class="py-3 mt-5 mb-3">遺言書の種類と効果</h3>
    <p>遺言を記した遺言書には「公正証書遺言」「自筆証書遺言」「秘密証書遺言」の３種類があります。主に利用されているのは公正証書遺言と自筆証書遺言です。いずれを選択するかは何を優先するかによります。それぞれのメリット・デメリットを通じて特徴を把握しましょう。</p>

    <h4 class="pt-4 pb-2">公正証書遺言</h4>
    <p>公正証書遺言とは、法律の専門家である公証人に依頼して作成する遺言書です。公証人が遺言者から遺言の内容を聞き取り、法律に定められた方式に則って遺言書を作成します。したがって法律的に確実に有効な遺言書が作成でき、不備により遺言書が無効になるおそれが極めて低いのが最大の特徴でありメリットです。信頼性が高く安心感のある遺言書だといえます。<br><br>
他にも次のようなメリットがあります。<br><br>
遺言者に遺言能力があるかも公証人が確認するので、後から遺言能力の有無で争いが起こるケースはほとんどありません。遺言書の原本は公証役場に保管されるので紛失や改ざんのおそれがないことに加え、全国どこの公証役場でも亡くなった人が遺言書を作成したかを検索でき、遺言者の相続人、受遺者、遺言執行者などの利害関係者は遺言書の写し(謄本)の再発行を、原本が保管されている公証役場に依頼することができます。<br><br>
一方、デメリットはメリットと表裏一体。公証人に遺言書の作成を依頼するため、必要書類の準備などの手間がかかります。また、財産の額に応じた手数料もかかります（下表）。公証役場に出向く際には、親族以外で証人２人を立てる必要があります。証人立会いのもとで公証人が遺言書を作成するため、遺言者の財産の内容が明らかになってしまいます。</p>

    <h5 class="mt-2">公正証書遺言のメリット・デメリット</h5>
    <h6 class="mt-2">メリット</h6>
    <ol>
      <li>公証人が作成するので法律的に確実に有効な遺言書が作成できる。</li>
      <li>遺言者の遺言能力の有無を問う争いが起こりにくい。</li>
      <li>遺言書の原本が公証役場に保管されるため、紛失・改ざんが起こりにくい。</li>
      <li>公証役場で検索すれば相続人は遺言書を見つけられ、写しの再発行が受けられる。</li>
    </ol>

    <h6 class="mt-2">デメリット</h6>
    <ol>
      <li>公証人に依頼するため手間と手数料がかかる</li>
      <li>証人（親族以外）を２人立てる必要があるため手間がかかる。</li>
      <li>公証人と証人に財産の内容が知られる。</li>
    </ol>

